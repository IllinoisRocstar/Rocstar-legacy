SUBROUTINE RFLO_GenerateCoarseGrids( region )

  USE ModDataTypes
  USE ModDataStruct, ONLY : t_region
  USE ModInterfaces, ONLY : RFLO_GetDimensPhysNodes, RFLO_GetNodeOffset
  USE ModError
  USE ModParameters
  IMPLICIT NONE

#include "Indexing.h"

! ... parameters
  TYPE(t_region) :: region

! ... loop variables
  INTEGER :: iLev, i, j, k

! ... local variables
  INTEGER :: ipnbegF, ipnendF, jpnbegF, jpnendF, kpnbegF, kpnendF
  INTEGER :: ipnbegC, ipnendC, jpnbegC, jpnendC, kpnbegC, kpnendC
  INTEGER :: iOffFine, ijOffFine, iOffCoarse, ijOffCoarse, ijkF, ijkC
  INTEGER :: ii, jj, kk

  REAL(RFREAL), POINTER :: xyzFine(:,:), xyzCoarse(:,:)

!******************************************************************************

  CALL RegisterFunction( region%global,'RFLO_GenerateCoarseGrids',&
  'RFLO_GenerateCoarseGrids.F90' )

! loop over higher levels (>1)

  DO iLev=2,region%nGridLevels

    xyzFine   => region%levels(iLev-1)%grid%xyz
    xyzCoarse => region%levels(iLev  )%grid%xyz

    CALL RFLO_GetDimensPhysNodes( region,iLev-1,ipnbegF,ipnendF, &
                                  jpnbegF,jpnendF,kpnbegF,kpnendF )
    CALL RFLO_GetDimensPhysNodes( region,iLev  ,ipnbegC,ipnendC, &
                                  jpnbegC,jpnendC,kpnbegC,kpnendC )
    CALL RFLO_GetNodeOffset( region,iLev-1,iOffFine  ,ijOffFine   )
    CALL RFLO_GetNodeOffset( region,iLev  ,iOffCoarse,ijOffCoarse )

    DO k=kpnbegC,kpnendC
      kk = (k-kpnbegC)*2 + kpnbegF
      DO j=jpnbegC,jpnendC
        jj = (j-jpnbegC)*2 + jpnbegF
        DO i=ipnbegC,ipnendC
          ii   = (i-ipnbegC)*2 + ipnbegF
          ijkF = IndIJK(ii,jj,kk,iOffFine  ,ijOffFine  )
          ijkC = IndIJK(i ,j ,k ,iOffCoarse,ijOffCoarse)
          xyzCoarse(XCOORD,ijkC) = xyzFine(XCOORD,ijkF)
          xyzCoarse(YCOORD,ijkC) = xyzFine(YCOORD,ijkF)
          xyzCoarse(ZCOORD,ijkC) = xyzFine(ZCOORD,ijkF)
        ENDDO  ! i
      ENDDO    ! j
    ENDDO      ! k

  ENDDO        ! iLev

! finalize

  CALL DeregisterFunction( region%global )

END SUBROUTINE RFLO_GenerateCoarseGrids

