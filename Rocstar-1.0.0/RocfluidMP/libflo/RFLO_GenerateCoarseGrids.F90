!
! Purpose: generate coarse grids from the finest grid.
!
! Description: the coarse grids are generated by omitting every second
!              grid node in all three directions (standard coarsening).
!
! Input: region = current region.
!
! Output: region%levels%grid%xyz = grid coordinates.
!
! Notes: none.
!
!******************************************************************************
!
! $Id: RFLO_GenerateCoarseGrids.F90,v 1.3 2008/12/06 08:44:06 mtcampbe Exp $
!
! Copyright: (c) 2001 by the University of Illinois
!
